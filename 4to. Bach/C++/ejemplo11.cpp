#include <iostream>
#include <string>

using namespace std;

int main(void){
    //Imprimir los numeros pares entre el 0 y 100
    cout << "Impresion de los numeros pares" << endl;
    for(int i=0; i<=100; i=i+2){
        cout << i << endl;
    }

    // IMPRIMIR LOS NUMEROS IMPARES ENTRE 1 Y 100
    cout << "impresion de numeros impares" << endl;
    for(int i=1; i<=100; i=i+2){
        cout << i << endl; 
    }
    
    

}